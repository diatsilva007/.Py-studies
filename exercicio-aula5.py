# EXERC: crie um programa que peça ao usuário que insira o seu nome, seu telefone e seu número de identidade.
        # ao final imprima as informações inforamdas pelo usuário

# A função 'input' recebe um texto como entrada e retorna esse texto como uma string.

nome = input("Digite o seu nome: ")

telefone = input("Digite seu telefone: ")

identidade = input("Digite seu número de identidade: ")

print("Seu nome é: ", nome + " Seu telefone é: " + telefone + " Sua identidade é: " + identidade)
